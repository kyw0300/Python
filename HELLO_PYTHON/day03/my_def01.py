def add(a,b):
    return a+b

def minus(a,b):
    return a-b

def multiply(a,b):
    return a*b

def divide(a,b):
    return a/b

def nameogi(a,b):
    return a%b

sum = add(4,3)
min = minus(4,3)
mul = multiply(4,3)
div = divide(4,3)
mod = nameogi(4,3)

print("sum",sum)
